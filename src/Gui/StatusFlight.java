    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
                int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String status = model.getValueAt(index, 0).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + status + " FlightStatus", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            FlightStatusDao.delete(status);
            setVisible(false);
            new StatusFlight().setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<FlightStatus> list = FlightStatusDao.getAllRecords();
        Iterator<FlightStatus> itr = list.iterator();
        while (itr.hasNext()) {
            FlightStatus flightStatusObj = itr.next();
            dtm.addRow(new Object[]{flightStatusObj.getStatus()});
        }
    }//GEN-LAST:event_formComponentShown

