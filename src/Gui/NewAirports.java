    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Airports> list = AirportsDao.getAllRecords();
        Iterator<Airports> itr = list.iterator();
        while (itr.hasNext()) {
            Airports airportsObj = itr.next();
            dtm.addRow(new Object[]{airportsObj.getId(), airportsObj.getIATA(), airportsObj.getAirports(),airportsObj.getCity(),airportsObj.getTimezone()});
        }
    }//GEN-LAST:event_formComponentShown

