    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        String IATA = model.getValueAt(index, 1).toString();
        String Name = model.getValueAt(index, 2).toString();
        String City = model.getValueAt(index, 3).toString();
        String Timezone = model.getValueAt(index, 4).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + Name + " Airport", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            AirportsDao.delete(id);
            setVisible(false);
            new NewAirports().setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Airports airports = new Airports();
        airports.setIATA(txtCode.getText());
        airports.setAirports(txtName.getText());
        airports.setCity(txtCity.getText());
        airports.setTimezone(txtTimezone.getText());
        AirportsDao.save(airports);
        setVisible(false);
        new NewAirports().setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed

