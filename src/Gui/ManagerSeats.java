    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<FareConditions> lis = FareConditionsDao.getAllRecords();
        Iterator<FareConditions> it = lis.iterator();
        while (it.hasNext()) {
            FareConditions fareConditionsObj = it.next();
            txtFareConditions.addItem(fareConditionsObj.getFareConditions());
        }
        ArrayList<AircraftCode> list = AircraftCodeDao.getAllRecords();
        Iterator<AircraftCode> itr = list.iterator();
        while (itr.hasNext()) {
            AircraftCode aircraftCodesObj = itr.next();
            txtAirCraftCode.addItem(aircraftCodesObj.getAircraftCode());
        }
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<BookingsSeats> list1 = BookingsSeatsDao.getAllRecords();
        Iterator<BookingsSeats> itr1 = list1.iterator();
        while (itr1.hasNext()) {
            BookingsSeats bookingsSeatsObj = itr1.next();
            dtm.addRow(new Object[]{bookingsSeatsObj.getAirCraftCode(), bookingsSeatsObj.getSeatNo(),bookingsSeatsObj.getFareConditions()});
        }
    }//GEN-LAST:event_formComponentShown

